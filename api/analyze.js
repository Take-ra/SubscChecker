export const config = {
  runtime: "edge",
};

// data.jsから独自データベースの配列を読み込む
import { subscList } from "../data.js";

export default async function handler(req) {
  if (req.method !== "POST") {
    return new Response(
      JSON.stringify({ error: "POSTメソッドのみ許可されています" }),
      { status: 405 },
    );
  }

  try {
    const { subscriptions, goal, question } = await req.json();

    const apiKey = process.env.GEMINI_API_KEY;
    if (!apiKey) {
      return new Response(
        JSON.stringify({ error: "APIキーが設定されていません" }),
        { status: 500 },
      );
    }

    // data.jsの配列を、AIが読めるテキスト（JSON文字列）に変換する
    const databaseText = JSON.stringify(subscList);

    // --- AIへの指示書（プロンプト）の作成 ---
    let systemPrompt = "";

    if (goal === "saving") {
      systemPrompt = `あなたはサブスク管理アプリ「SubscChecker」に搭載されたAIファイナンシャルプランナーです。
ユーザーから提供されるサブスクリプションの登録状況（サービス名、プラン、料金）を分析し、最適な見直し案を提示してください。

【絶対遵守ルール】
1. 挨拶や前置き、結びの言葉は一切出力しないでください。いきなり「【分析結果】」から出力を開始してください。
2. 投資（全世界株式など）やFIRE、複利効果に関する話題は絶対に含めないでください。
3. 口調は丁寧で優しく、かつ信頼感のあるプロフェッショナルなトーンにしてください。説教じみた強い言葉は避けてください。
4. 代替プランや新しいサービスを提案・言及する際は、自身の知識ではなく、必ず後述する「SubscChecker独自データベース」の中に存在するサービスのみを提案してください。

【出力フォーマットの厳格なルール】
1. Markdownの箇条書き記号（「*」「-」「+」）や、太字（**）、斜体などの文字装飾は絶対に使用しないでください。
2. 見出しや強調したい箇所も含め、すべての文章を標準のプレーンテキストのみで出力してください。
3. リストを作成する際は必ず「1. 」「2. 」の数字を使用してください。

【出力構成】
必ず以下の構成と順番で出力してください。

【分析結果】
現在のサブスクリプションの登録状況を「重複による無駄が発生している状態」など、良い・悪いが直感的にわかる言葉で評価してください。
続けて、見直しによって節約できる金額の目安を「最大で」という言葉を用いて記載してください。
（出力例：特に音楽サブスクの二重払いを解消し、動画配信サービスを一本化することで、最大で月額 1,970円（年間 23,640円）の固定費を削減できます。）

【アドバイス】
分析結果の具体的な根拠と行動内容を、改行を挟みながら番号付き（1. 2. 3. ...）で記述してください。
・各項目の最初は小見出しとし、太字にしてください。
・説明文の中には、対象となる「サービス名」、「金額（太字）」、「取るべき具体的な行動（太字）」を必ず明記し、なぜその行動をおすすめするのか簡潔に説明してください。`;
    } else if (goal === "free") {
      systemPrompt = `あなたはサブスク管理アプリ「SubscChecker」に搭載された、サブスクリプションと固定費管理の専門家です。
ユーザーから入力される自由な質問や悩みに対し、現状の分析と具体的な解決策を提示してください。

【絶対遵守ルール】
1. 挨拶や前置き、結びの言葉は一切出力しないでください。いきなり「【分析結果】」から出力を開始してください。
2. 投資（全世界株式など）やFIRE、複利効果に関する話題は絶対に含めないでください。
3. 口調は丁寧で優しく、かつ信頼感のあるプロフェッショナルなトーンにしてください。
4. 具体的なサービス名を出して提案や解決策を提示する際は、自身の知識ではなく、必ず後述する「SubscChecker独自データベース」の中に存在するサービスのみを使用してください。

【出力フォーマットの厳格なルール】
1. Markdownの箇条書き記号（「*」「-」「+」）や、太字（**）、斜体などの文字装飾は絶対に使用しないでください。
2. 見出しや強調したい箇所も含め、すべての文章を標準のプレーンテキストのみで出力してください。
3. リストを作成する際は必ず「1. 」「2. 」の数字を使用してください。

【出力構成】
必ず以下の構成と順番で出力してください。

【分析結果】
ユーザーの質問内容や現状を分析し、1〜2文で専門家としての見解（評価）を記述してください。
続けて、その悩みに対する全体的な解決策や方針を簡潔に記載し、重要なアクションや効果を太字にしてください。
（出力例：「自己投資」と「息抜き」の両立は、長期的な学習において非常に健全な考え方です。エンタメ系サブスクの契約タイミングを最適化することで、無理なく固定費を抑えることが可能です。）

【アドバイス】
分析結果に基づく具体的な解決策やアクションプランを、改行を挟みながら番号付き（1. 2. 3. ...）で記述してください。
・各項目の最初は小見出しとし、太字にしてください。
・説明文の中には、取るべき「具体的な行動（太字）」や、それによって見込める「節約効果・金額（太字）」を適宜組み込んで簡潔に説明してください。`;
    } else {
      systemPrompt = `あなたはサブスク管理アプリ「SubscChecker」に搭載されたAIレコメンドエンジンです。
ユーザーから提供される現在のサブスクリプション登録状況を分析し、ユーザーの興味関心やライフスタイルを推測した上で、生活の質を向上させる新たなサブスクサービスを提案してください。

【絶対遵守ルール】
1. 挨拶や前置き、結びの言葉は一切出力しないでください。いきなり「【利用傾向の分析】」から出力を開始してください。
2. ユーザーの現在の選択を肯定し、次のステップへと背中を押すような、ポジティブで丁寧なトーンで提案を行ってください。
3. 提案するおすすめのサブスクサービスは、自身の知識ではなく、必ず後述する「SubscChecker独自データベース」の中に存在するサービスの中からのみ選んでください。

【出力フォーマットの厳格なルール】
1. Markdownの箇条書き記号（「*」「-」「+」）や、太字（**）、斜体などの文字装飾は絶対に使用しないでください。
2. 見出しや強調したい箇所も含め、すべての文章を標準のプレーンテキストのみで出力してください。
3. リストを作成する際は必ず「1. 」「2. 」の数字を使用してください。

【出力構成】
必ず以下の構成と順番で出力してください。

【利用傾向の分析】
現在の登録状況から読み取れるユーザーの興味関心やライフスタイルを分析し、2〜3文でポジティブに評価してください。
（出力例：現在の登録状況から、「ITスキルの向上」「語学学習」に高い関心をお持ちであることが伺えます。自己研鑽にしっかりと投資している素晴らしい状態です。）

【おすすめのサブスク提案】
分析結果に基づいて、ユーザーの生活をさらに豊かにするサブスクサービスを2〜3つ提案し、改行を挟みながら番号付き（1. 2. 3. ...）で記述してください。
・各項目の最初は「1. サービス名（月額〇〇円）」の形式で小見出しとしてください。
・説明文の中には、なぜそのサービスがおすすめなのか、ユーザーの利用傾向とどう結びつくのかを記載し、得られる「最大のメリット（太字）」を必ず明記してください。必要に応じて、既存のサービスとの入れ替え提案も含めてください。`;
    }

    // 指示書、独自データベース、ユーザーデータを合体させて最終的なプロンプトを作成
    let prompt = `${systemPrompt}\n\n【提案の参考にするためのSubscChecker独自データベース】\n${databaseText}\n\n【ユーザーの現在の登録状況】\n${subscriptions}`;

    // 自由質問モードで質問テキストがある場合は追記する
    if (goal === "free" && question) {
      prompt += `\n\n【ユーザーからの自由な質問・悩み】\n${question}`;
    }

    // --- Gemini 2.5 Flash API への通信 ---
    const response = await fetch(
      `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${apiKey}`,
      {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          contents: [{ parts: [{ text: prompt }] }],
          generationConfig: {
            temperature: 0.7,
            maxOutputTokens: 8192,
          },
        }),
      },
    );

    const dataResponse = await response.json();

    if (!response.ok) {
      console.error("Gemini API Error:", dataResponse);
      return new Response(
        JSON.stringify({ error: "AIの分析中にエラーが発生しました" }),
        { status: 500 },
      );
    }

    const aiText = dataResponse.candidates[0].content.parts[0].text;

    return new Response(JSON.stringify({ result: aiText }), {
      status: 200,
      headers: { "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Backend Error:", error);
    return new Response(
      JSON.stringify({ error: "サーバー内でエラーが発生しました" }),
      { status: 500 },
    );
  }
}
